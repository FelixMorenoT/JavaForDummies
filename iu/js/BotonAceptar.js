function resultados(){

   var bien=0;
   var mal=0;

   for (var i = 1; i <=10; i++) {
      if (document.getElementById('CyX'+i).getAttribute('src')=="iu/image/check2.png") {
         bien++;
      }else{
         mal++;
      };
   };
   var conteo= verificarFinal();
   alert("Respuestas buenas: "+bien+"\nRespuestas Malas: "+mal +"\nFaltan " + conteo+" Pregunta(s) Por Responder");
   if (bien>=6) {
      alert("¡¡¡Aprobo El Taller :D !!!")
      createCookie("Test2",100,1);
   }
   else
   {
      alert("¡¡¡Reprobo El Taller D: !!!")
   };
}


function verificarFinal()
{
   var A=document.getElementsByClassName('boton');
   var cont=0;
   for (var j = 0; j < A.length; j++) {
      if (A[j].checked==false && A[j].disabled==false) {
         cont++;
      };
   };
   return cont/4;
}

